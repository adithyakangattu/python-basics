#-Calculate the area of a rectangle given width and height
#using return statement

def rectangle_area(width, height):
    return width * height

print(rectangle_area(5,3))
print(rectangle_area(7,2))
print(rectangle_area(10,5))


#2-Check if a number is even and return True or False
#using return statement



def is_even(number):
    return number % 2 == 0

print(is_even(4))
print(is_even(7))
print(is_even(10))